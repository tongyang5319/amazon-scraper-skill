def random_user_agent() -> str:
    """Return a random User-Agent string."""
    return random.choice(USER_AGENTS)


def random_delay(min_seconds: float = 8.0, max_seconds: float = 15.0) -> None:
    """
    Sleep for a random duration between min_seconds and max_seconds.
    Adds small jitter to avoid predictable patterns.
    """
    delay = random.uniform(min_seconds, max_seconds)
    time.sleep(delay)


def random_jitter(base_seconds: float = 1.0) -> None:
    """Add a small random jitter (0 to base_seconds)."""
    time.sleep(random.uniform(0, base_seconds))


def with_random_delay(min_d: float = 8.0, max_d: float = 15.0):
    """Decorator that adds a random delay before executing a function."""
    def decorator(func: Callable):
        def wrapper(*args, **kwargs):
            random_delay(min_d, max_d)
            return func(*args, **kwargs)
        return wrapper
    return decorator


def retry_on_exception(max_retries: int = 3, base_delay: float = 5.0):
    """
    Decorator that retries the wrapped function on exception,
    with exponential backoff and jitter.
    """
    def decorator(func: Callable):
        def wrapper(*args, **kwargs):
            for attempt in range(max_retries):
                try:
                    return func(*args, **kwargs)
                except Exception:
                    if attempt == max_retries - 1:
                        raise
                    delay = base_delay * (2 ** attempt) + random.uniform(0, 3)
                    time.sleep(delay)
            return None
        return wrapper
    return decorator
